@pseudo_label_group.command("inspect")
@click.argument("run", default=None, required=False)
@click.option(
    "--input-dir",
    default=None,
    type=click.Path(exists=True, path_type=Path),
    help="Inspect an arbitrary directory (with images/train/ + labels/train/) instead of a run.",
)
@click.option(
    "--output-dir",
    default=None,
    type=click.Path(),
    help="Output directory for annotated PNGs. Defaults to {run_dir}/pseudo_labels/viz/ or {input_dir}/viz/.",
)
@click.option(
    "-n",
    "n_samples",
    default=None,
    type=int,
    help="Number of images to sample randomly per subset. Omit to inspect all.",
)
@click.option(
    "--seed",
    default=42,
    type=int,
    show_default=True,
    help="Random seed for sampling.",
)
@click.pass_context
def inspect(
    ctx: click.Context,
    run: str | None,
    input_dir: Path | None,
    output_dir: str | None,
    n_samples: int | None,
    seed: int,
) -> None:
    """Visualize pseudo-label outputs from a pipeline run or arbitrary directory.

    When ``--input-dir`` is provided, inspects that single directory directly
    (must contain ``images/train/`` and ``labels/train/``). Otherwise resolves
    a pipeline run and inspects all pseudo-label subsets.
    """
    if input_dir is not None:
        # Single directory mode — inspect all splits (train, val)
        base_out = Path(output_dir) if output_dir else input_dir / "viz"
        total = 0
        for split in ("train", "val"):
            if (input_dir / "images" / split).exists():
                count = _inspect_subset(
                    input_dir, base_out / split, n_samples, seed, split=split
                )
                total += count
        if total == 0:
            # Fallback: try without split subdirectory
            total = _inspect_subset(input_dir, base_out, n_samples, seed)
        click.echo(f"Wrote {total} annotated images to {base_out}")
        return

    from aquapose.cli_utils import get_project_dir, resolve_run

    run_dir = resolve_run(run, get_project_dir(ctx))
    pseudo_dir = run_dir / "pseudo_labels"
    if not pseudo_dir.exists():
        raise click.ClickException(
            f"No pseudo_labels/ directory found in {run_dir}. "
            "Run 'aquapose pseudo-label generate' first."
        )

    base_out = Path(output_dir) if output_dir else pseudo_dir / "viz"

    # Discover all pseudo-label subsets to inspect
    inspect_targets: list[tuple[Path, Path]] = []
    for candidate, viz_subdir in [
        (pseudo_dir / "obb", base_out / "obb"),
        (pseudo_dir / "pose" / "consensus", base_out / "pose" / "consensus"),
        (pseudo_dir / "pose" / "gap", base_out / "pose" / "gap"),
    ]:
        images_check = candidate / "images" / "train"
        if images_check.exists() and any(images_check.iterdir()):
            inspect_targets.append((candidate, viz_subdir))

    if not inspect_targets:
        raise click.ClickException(
            f"No pseudo-label image directories found in {pseudo_dir}"
        )

    total_written = 0
    for data_path, out_path in inspect_targets:
        count = _inspect_subset(data_path, out_path, n_samples, seed)
        total_written += count

    click.echo(f"Wrote {total_written} annotated images to {base_out}")


def _inspect_subset(
    data_path: Path,
    out_path: Path,
    n_samples: int | None,
    seed: int,
    split: str | None = None,
) -> int:
    """Inspect a single pseudo-label subset directory.

    Args:
        data_path: YOLO-format directory containing images/ and labels/.
        out_path: Output directory for annotated PNGs.
        n_samples: Number of images to sample randomly. None for all.
        seed: Random seed for sampling.
        split: Specific split to inspect ("train", "val"). If None, tries
            "train" then falls back to flat images/.

    Returns:
        Number of images written.
    """
    import random

    out_path.mkdir(parents=True, exist_ok=True)

    # Find image/label directories
    if split is not None:
        images_dir = data_path / "images" / split
        labels_dir = data_path / "labels" / split
    else:
        images_dir = data_path / "images" / "train"
        labels_dir = data_path / "labels" / "train"
        if not images_dir.exists():
            images_dir = data_path / "images"
        if not labels_dir.exists():
            labels_dir = data_path / "labels"

    if not images_dir.exists() or not labels_dir.exists():
        return 0

    # Collect image files that have matching label files
    image_files = sorted(
        p
        for p in images_dir.iterdir()
        if p.suffix.lower() in {".jpg", ".jpeg", ".png"}
        and (labels_dir / f"{p.stem}.txt").exists()
    )

    if not image_files:
        return 0

    # Sample if requested
    if n_samples is not None and n_samples < len(image_files):
        rng = random.Random(seed)
        image_files = rng.sample(image_files, n_samples)

    # Load confidence sidecar if available
    confidence_data: dict = {}
    for candidate in [
        data_path / "confidence.json",
        data_path.parent / "confidence.json",
    ]:
        if candidate.exists():
            confidence_data = json.loads(candidate.read_text())
            break

    # Detect label type from first label file
    first_label = (labels_dir / f"{image_files[0].stem}.txt").read_text().strip()
    first_tokens = first_label.split("\n")[0].split()
    is_obb = len(first_tokens) == 9  # cls + 4 corners x 2

    subset_name = data_path.relative_to(data_path.parent.parent)
    click.echo(
        f"Inspecting {len(image_files)} images from {subset_name} "
        f"({'OBB' if is_obb else 'pose'} labels)"
    )

    for img_path in image_files:
        image = cv2.imread(str(img_path))
        if image is None:
            logger.warning("Failed to read %s, skipping", img_path)
            continue

        img_h, img_w = image.shape[:2]
        label_path = labels_dir / f"{img_path.stem}.txt"
        label_text = label_path.read_text().strip()

        if not label_text:
            continue

        # Look up confidence metadata
        conf_entry = confidence_data.get(img_path.stem, {})
        fish_labels = conf_entry.get("labels", [])

        for line_idx, line in enumerate(label_text.split("\n")):
            tokens = line.split()
            if not tokens:
                continue

            # Get per-fish metadata if available
            fish_meta = fish_labels[line_idx] if line_idx < len(fish_labels) else {}
            confidence = fish_meta.get("confidence")
            gap_reason = fish_meta.get("gap_reason")

            if is_obb:
                _draw_obb_inspection(image, tokens, img_w, img_h)
            else:
                _draw_pose_inspection(image, tokens, img_w, img_h)

            # Build text overlay
            text_parts: list[str] = []
            if confidence is not None:
                text_parts.append(f"conf={confidence:.2f}")
            source = fish_meta.get("source")
            if source is not None:
                text_parts.append(source)
            elif gap_reason is not None:
                text_parts.append(f"gap:{gap_reason}")
            elif fish_meta:
                text_parts.append("consensus")

            if text_parts:
                text = " | ".join(text_parts)
                # Position text near the annotation
                if is_obb:
                    tx = int(float(tokens[1]) * img_w)
                    ty = max(15, int(float(tokens[2]) * img_h) - 10)
                else:
                    tx = max(
                        5, int(float(tokens[1]) * img_w - float(tokens[3]) * img_w / 2)
                    )
                    ty = max(
                        15,
                        int(float(tokens[2]) * img_h - float(tokens[4]) * img_h / 2)
                        - 10,
                    )

                cv2.putText(
                    image,
                    text,
                    (tx, ty),
                    cv2.FONT_HERSHEY_SIMPLEX,
                    0.45,
                    (255, 255, 255),
                    2,
                )
                cv2.putText(
                    image,
                    text,
                    (tx, ty),
                    cv2.FONT_HERSHEY_SIMPLEX,
                    0.45,
                    (0, 200, 0),
                    1,
                )

        cv2.imwrite(str(out_path / f"{img_path.stem}.png"), image)

    return len(image_files)


def _draw_obb_inspection(
    image: np.ndarray,
    tokens: list[str],
    img_w: int,
    img_h: int,
) -> None:
    """Draw an OBB polygon from a YOLO-OBB label line."""
    # tokens: cls x1 y1 x2 y2 x3 y3 x4 y4 (normalized)
    corners = np.array(
        [
            [float(tokens[1]) * img_w, float(tokens[2]) * img_h],
            [float(tokens[3]) * img_w, float(tokens[4]) * img_h],
            [float(tokens[5]) * img_w, float(tokens[6]) * img_h],
            [float(tokens[7]) * img_w, float(tokens[8]) * img_h],
        ],
        dtype=np.int32,
    )
    cv2.polylines(image, [corners.reshape(-1, 1, 2)], True, (0, 255, 0), 2)


def _draw_pose_inspection(
    image: np.ndarray,
    tokens: list[str],
    img_w: int,
    img_h: int,
) -> None:
    """Draw a pose bbox and keypoints from a YOLO-Pose label line."""
    # tokens: cls cx cy w h x1 y1 v1 x2 y2 v2 ...
    cx = float(tokens[1]) * img_w
    cy = float(tokens[2]) * img_h
    bw = float(tokens[3]) * img_w
    bh = float(tokens[4]) * img_h
    x1 = int(cx - bw / 2)
    y1 = int(cy - bh / 2)
    x2 = int(cx + bw / 2)
    y2 = int(cy + bh / 2)
    cv2.rectangle(image, (x1, y1), (x2, y2), (0, 255, 0), 2)

    # Draw keypoints
    kp_tokens = tokens[5:]
    for i in range(0, len(kp_tokens), 3):
        if i + 2 >= len(kp_tokens):
            break
        kx = float(kp_tokens[i]) * img_w
        ky = float(kp_tokens[i + 1]) * img_h
        vis = int(float(kp_tokens[i + 2]))
        if vis > 0:
            color = (0, 0, 255) if i == 0 else (0, 255, 0)
            cv2.circle(image, (int(kx), int(ky)), 4, color, -1)
